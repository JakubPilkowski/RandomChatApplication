package com.example.randomchatapplication.models;

public class SpinnerItem {
    private String key;
    private String value;

    public SpinnerItem(String key, String value) {
        this.key = key;
        this.value = value;

    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }

}
